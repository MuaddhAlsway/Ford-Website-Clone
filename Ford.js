function Content(){

  let control = document.getElementById('control')
  let ContentID = document.getElementById('ContentId')
  ContentID.style.display = 'none'

  document.querySelector('.fa-plus').addEventListener('click', function() {
     
    if(this.classList.contains('fa-plus')){
        this.classList.remove('fa-plus')
        this.classList.add('fa-minus')
    } else{
        this.classList.remove('fa-minus')
        this.classList.add('fa-plus')
    }
 
});

  control.addEventListener('click', ()=>{
    if(ContentID.style.display === 'none' ){
      ContentID.style.display = 'inline'
      
    } else {
      ContentID.style.display = 'none'
    }
  })
}

