  function PowerUp() {
    let ChooseYourPower = document.getElementById('ChooseYourPower');
    let ContainerPowerId = document.getElementById('PowerUp');
  
    // Initialize the display state to 'none'
    ContainerPowerId.style.display = 'inline';
  
    ChooseYourPower.addEventListener('click', () => {
      document.getElementById('MustangMuscluar').style.display = 'none';
      document.getElementById('HybridCar').style.display = 'none';
      document.getElementById('AllElectric').style.display = 'none';
      document.getElementById('Desil').style.display = 'none';

      // Toggle the display style between 'none' and 'inline'
      if (ContainerPowerId.style.display === 'none') {
        ContainerPowerId.style.display = 'inline'; // Show the element
      } else {
        ContainerPowerId.style.display = 'none'; // Hide the element
      }
    });
  }


